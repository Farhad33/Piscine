char	*ft_rot42(char *str)
{
	int i;
	int	count;

	i = 0;
	while (str[i])
	{
		count = 48;
		if (str[i] >= 65 && str[i] <= 90)
		{
			while (count--)
			{
				str[i] == 90 ? str[i] = 65 : str[i]++;
			}
		}
		else if (str[i] >= 97 && str[i] <= 122)
		{
			while (count--)
			{
				str[i] == 122 ? str[i] = 97 : str[i]++;
			}
		}
		i++;
	}
	return (str);
}
